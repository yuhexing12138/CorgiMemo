package com.corgimemo.app.animation

/**
 * 柯基动画类型枚举
 * 定义所有可用的动画类型
 */
enum class AnimationType {
    /** 坐立姿态 */
    SIT,
    /** 站立姿态 */
    STAND,
    /** 趴卧姿态 */
    LIE,
    /** 奔跑姿态 */
    RUN,
    /** 眨眼动作 */
    WINK,
    /** 摇尾巴动作 */
    WAG,
    /** 歪头动作 */
    TILT,
    /** 睡觉姿态 */
    SLEEP,
    /** 难过表情 */
    SAD,
    /** 骄傲表情 */
    PROUD,
    /** 害羞表情 */
    SHY,
    /** 担心表情 */
    WORRY,
    /** 打滚动作 */
    ROLL
}
